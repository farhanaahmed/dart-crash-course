void main(List<String> args) {
  //binary infix operators
  const num = 50;
  print(num);
  print(num + 20);
  print(num - 20);
  print(num * 20);
  print(num / 20);
  print(num ~/ 20);
  print(num % 20);
  print(num == 20);
  print(num != 20);
  print(num > 20);
  print(num < 20);
  print(num >= 20);
  print(num <= 20);

  //bitwise infix operators

  //bitwise AND (&) operator
  print(10 & 184);

  //bitwise OR (|) operator
  print(10 | 184);

  //bitwise XOR (|) operator
  print(10 ^ 184);

  //bitwise left shift (<<) operator

  print(99 << 1);
  print(99 << 2);
  print(99 >> 1);
  print(99 >> 2);
}
